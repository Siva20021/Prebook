const seats = Array.from({ length: 8 * 8 }, (_, i) => i)
const SeatBooking = () => {
  const [selectedMovie, setSelectedMovie] = useState(movies[0])
  const [selectedSeats, setSelectedSeats] = useState([])
  const [info, setInfo] = useState("");
  const { movieid } = useParams();
  movieID = movieid;
  const url = `https://api.themoviedb.org/3/movie/${movieID}?api_key=${process.env.REACT_APP_API_KEY}&language=en-US`;
  const getMovieDetails = () =>
    axios.get(url).then(function (response) {
      const info = response.data;
      setInfo(info);
      setSelectedMovie(info)
    });
  useEffect(() => {
    getMovieDetails();
  }, []);

  return (
  <div className="App">
  <Movies
    movie={selectedMovie}
    onChange={movie => {
      setSelectedSeats([])
      setSelectedMovie(movie)
    }}
  />
  <ShowCase />
  <Cinema
    movie={selectedMovie}
    selectedSeats={selectedSeats}
    onSelectedSeatsChange={selectedSeats => setSelectedSeats(selectedSeats)}
  />

  <p className="info">
    You have selected <span className="count">{selectedSeats.length}</span>{' '}
    seats for the price of{' '}
    <span className="total">
      {selectedSeats.length * selectedMovie.price}$
    </span>
  </p>
</div>);
};

function Movies({ movie, onChange }) {
  return (
    <div className="Movies">
      <label htmlFor="movie">Pick a movie</label>
      <select
        id="movie"
        value={movie.name}
        onChange={e => {
          onChange(movies.find(movie => movie.name === e.target.value))
        }}
      >
        {movies.map(movie => (
          <option key={movie.name} value={movie.name}>
            {movie.name} (${movie.price})
          </option>
        ))}
      </select>
    </div>
  )
}

function ShowCase() {
  return (
    <ul className="ShowCase">
      <li>
        <span className="seat" /> <small>N/A</small>
      </li>
      <li>
        <span className="seat selected" /> <small>Selected</small>
      </li>
      <li>
        <span className="seat occupied" /> <small>Occupied</small>
      </li>
    </ul>
  )
}

function Cinema({ movie, selectedSeats, onSelectedSeatsChange }) {
  function handleSelectedState(seat) {
    const isSelected = selectedSeats.includes(seat)
    if (isSelected) {
      onSelectedSeatsChange(
        selectedSeats.filter(selectedSeat => selectedSeat !== seat),
      )
    } else {
      onSelectedSeatsChange([...selectedSeats, seat])
    }
  }

  window.onload = async function() {
    if (window.ethereum) {
      console.log("This is DAppp Environment");
      var accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
      var currentaddress = accounts[0];
    } else {
      console.log("Please connect with metamask");
    }
  }
  
  marketplaceContract.methods.mint(movieID, seatNo).send({ from: currentaddress }).then(() => {
    alert("stored success!");
  }).catch((err) => {
    console.log(err);
  })
  
  let seatsNotAvail = marketplaceContract.methods.getSeatsNotAvail(movieID,seatNo).send({ from: currentaddress }).then((res) => {
    console.log("res");
  }).catch((err) => {
    console.log(err);
  })

  return (
    <div className="Cinema">
      <div className="screen" />

      <div className="seats">
        {seats.map(seat => {
          const isSelected = selectedSeats.includes(seat)
          const isOccupied = movie.occupied.includes(seat)
          return (
            <span
              tabIndex="0"
              key={seat}
              className={clsx(
                'seat',
                isSelected && 'selected',
                isOccupied && 'occupied',
              )}
              onClick={isOccupied ? null : () => handleSelectedState(seat)}
              onKeyPress={
                isOccupied
                  ? null
                  : e => {
                      if (e.key === 'Enter') {
                        handleSelectedState(seat)
                      }
                    }
              }
            />
          )
        })}
      </div>
    </div>
  )
}




export default SeatBooking;
